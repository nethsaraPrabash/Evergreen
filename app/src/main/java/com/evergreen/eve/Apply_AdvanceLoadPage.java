package com.evergreen.eve;

import android.content.Intent;
import android.os.Bundle;
import android.widget.ImageView;

import androidx.activity.EdgeToEdge;
import androidx.appcompat.app.AppCompatActivity;
import androidx.core.graphics.Insets;
import androidx.core.view.ViewCompat;
import androidx.core.view.WindowInsetsCompat;

public class Apply_AdvanceLoadPage extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_apply_advance_load_page);
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main), (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });

        Apply_AdvanceMain apply_advanceMain = new Apply_AdvanceMain();
        ImageView imageView = findViewById(R.id.loadApplyAdvanceBack);

        imageView.setOnClickListener(v -> {
            startActivity(new Intent(Apply_AdvanceLoadPage.this, Apply_AdvanceMain.class));
        });
    }
}